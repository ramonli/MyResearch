package com.ramonli.sandbox.spring.aop;

public class OrderInfo {
	private String serialNo;
	private int count;

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
